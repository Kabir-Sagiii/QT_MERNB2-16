function fnAdd(num1, num2) {
  var x = num1;
  var y = num2;

  var result;
  result = x + y; //30 + 40

  return result;
}

var myResult1 = fnAdd(10, 20); // 10 20
console.log(myResult1);

var myResult2 = fnAdd(30, 40); // 30 40
console.log(myResult2);

var myresult3 = fnAdd(20, 20); // 20 20
console.log(myresult3);
